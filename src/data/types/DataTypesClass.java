package data.types;

public class DataTypesClass {

	public static void main(String[] args) {
		
		String data = "Welcome to KTCTC";
		System.out.println(data);
		data = "ABCD";
		System.out.println(data);
		
		
		System.out.println("----------------------------");
		char chVeri = 'D';
		System.out.println(chVeri);
		chVeri = '5';		
		System.out.println(chVeri);		
		System.out.println("----------------------------");
		boolean booleanVeri = true;
		System.out.println(booleanVeri);		
		System.out.println("----------------------------");
		double dbVeri = 55.77d;
		System.out.println(dbVeri);
		System.out.println("----------------------------");
		float flVeri = 2.5f;
		System.out.println(flVeri);		
		System.out.println("----------------------------");
		long longInt =  564546455756l;
		System.out.println(longInt);
		System.out.println("----------------------------");
		int intVeri = 567557575;
		System.out.println(intVeri);
		intVeri = 78;
		System.out.println(intVeri);		
		System.out.println("----------------------------");
		short shortVeri = 850;
		System.out.println(shortVeri);
		System.out.println("----------------------------");
		byte abc;			
		byte btVeri = 85;
		abc = 56;
		System.out.println(abc);
		System.out.println(btVeri);
		abc = 99;
		System.out.println(abc);
		System.out.println(btVeri);
		

	}

}
